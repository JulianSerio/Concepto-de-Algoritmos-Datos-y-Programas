1) ¿Qué se define como memoria estática?
2) ¿Qué se define como memoria dinámica?
3) ¿Qué es una variable de tipo puntero?
4) ¿Qué hace la operación “NEW” aplicada en una variable del tipo puntero?
5) ¿Qué hace la operación “DISPOSE” aplicada en una variable del tipo puntero?

1)  No permiten modificar su tamaño en tiempo de ejecucion.
    Las varibles y tipos reserven memoria en su declaracion 
    y se mantienen durante todo el programa.
    El leguaje puede validar previo a la ejecucion.

2) Permiten modificar en tiempo de ejecucion la memoria utilizada

3)  Es un tipo de variable usada para almacenar una direccion en memoria 
    se encuentra el valor que puede ser de cualquiera de los tipos vistos 
    (char, boolean, integer, real, string, registro, arreglo u puntero)
    Un puntero es un tipo de datos simple 

4)  Implica reservar una dirección memoria dinámica libre para poder asignarle 
    contenidos a la dirección que contiene la variable de tipo puntero.

5)  Implica liberar la memoria dinámica que contenía la variable de tipo puntero.